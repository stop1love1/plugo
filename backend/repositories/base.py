"""
Abstract repository interfaces.
All database operations go through these interfaces,
so the app doesn't care if the backend is SQLite or MongoDB.
"""

from abc import ABC, abstractmethod
from typing import Optional


class BaseSiteRepo(ABC):
    @abstractmethod
    async def create(self, data: dict) -> dict: ...
    @abstractmethod
    async def get_by_id(self, site_id: str) -> Optional[dict]: ...
    @abstractmethod
    async def get_by_token(self, token: str) -> Optional[dict]: ...
    @abstractmethod
    async def list_all(self) -> list[dict]: ...
    @abstractmethod
    async def update(self, site_id: str, data: dict) -> Optional[dict]: ...
    @abstractmethod
    async def delete(self, site_id: str) -> bool: ...


class BaseKnowledgeRepo(ABC):
    @abstractmethod
    async def create(self, data: dict) -> dict: ...
    @abstractmethod
    async def get_by_id(self, chunk_id: str) -> Optional[dict]: ...
    @abstractmethod
    async def list_by_site(self, site_id: str, page: int = 1, per_page: int = 20, search: Optional[str] = None) -> dict: ...
    @abstractmethod
    async def update(self, chunk_id: str, data: dict) -> Optional[dict]: ...
    @abstractmethod
    async def delete(self, chunk_id: str) -> bool: ...
    @abstractmethod
    async def create_many(self, chunks: list[dict]) -> list[str]: ...
    @abstractmethod
    async def delete_many(self, chunk_ids: list[str]) -> int: ...
    @abstractmethod
    async def get_many(self, chunk_ids: list[str]) -> list[dict]: ...
    @abstractmethod
    async def list_crawled_urls(self, site_id: str) -> list[dict]: ...
    @abstractmethod
    async def list_content_hashes(self, site_id: str) -> set[str]: ...
    @abstractmethod
    async def list_by_url(self, site_id: str, source_url: str) -> list[dict]: ...
    @abstractmethod
    async def delete_by_url(self, site_id: str, source_url: str) -> int: ...


