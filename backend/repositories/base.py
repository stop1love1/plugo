    async def delete(self, memory_id: str) -> bool:
        ...

    @abstractmethod
    async def delete_by_visitor(self, visitor_id: str, site_id: str) -> int:
        ...

    @abstractmethod
    async def list_by_site(self, site_id: str) -> list[dict]:
        ...


